    private void PreencherTabela() throws Exception{
        DefaultTableModel dtm = 
                new DefaultTableModel(){
                    @Override
                    public boolean 
                            isCellEditable
                            (int row,
                             int column){
                                return false;
                            }
                                    
                };
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/usuario","root","");
            String sql = "select * from produto";
            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery(sql);         
            dtm.addColumn("id");
            dtm.addColumn("nome");
            dtm.addColumn("preco");
            dtm.addColumn("tamanho");
            dtm.addColumn("ingredientes");
            dtm.addColumn("tipo");
            while(rs.next()){
                dtm.addRow(new String[]{
                   rs.getString("id"),
                   rs.getString("nome"),
                   rs.getString("preco"),
                   rs.getString("tamanho"),
                   rs.getString("ingredientes"),
                   rs.getString("tipo"),
                });
            }
            DefaultTableModel model;
            try{
                model = dtm;
                tabela.setModel(model);
            }
            catch(Exception ex){
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null,ex.getMessage());
        }
        
    }
    
    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
       if(cmbTipo.getSelectedItem() == "Bebida"){
            Produto produto = new Produto(); 

            produto.setNome(txtNome.getText());
            produto.setPreco(Double.parseDouble(txtPreco.getText()));
            produto.Create();
       }
       
       else if(cmbTipo.getSelectedItem() == "Massa"){
            Pizza pizza = new Pizza();

            pizza.setNome(txtNome.getText());
            pizza.setPreco(Double.parseDouble(txtPreco.getText()));
            pizza.setTamanho((String) cmbTamanho.getSelectedItem());
            pizza.setIngredientes(txtPreco.getText());
            pizza.Create();
       }
        
        try {
            this.PreencherTabela();
        } catch (Exception ex) {
            Logger.getLogger(CadastroCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

