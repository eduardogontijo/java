    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/usuario","root","");
            String sql = "select * from usuario where nome = ? and cpf = ? ";
            PreparedStatement stmt = conn.prepareStatement(sql);
            stmt.setString(1, txtPass.getText());
            stmt.setString(2, txtUser.getText());
            ResultSet rs = stmt.executeQuery();
            rs.last();
            int registro = rs.getRow();
            if(registro > 0){
                Menu menu = new Menu();
                menu.setVisible(true);
                this.dispose();
            }
            else{
                JOptionPane.showMessageDialog(null,"ERRO");
            }
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null,ex.getMessage());
        }
    }//GEN-LAST:event_btnLoginActionPerformed

